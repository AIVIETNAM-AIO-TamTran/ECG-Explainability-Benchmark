"""
Phase 1 · Build the MI-vs-NORM cohort.
See Decisions D4, D9 and docs/04_DATA_LABELS_AND_SAMPLING.md.

Logic:
  - Positive = any MI-superclass code (config.MI_CODES); Negative = pure NORM.
  - "strict"    : positive needs an MI code at likelihood == 100; negative is a
                  record whose only superclass is NORM.
  - "inclusive" : any MI code at any likelihood; negative unchanged.
  - Retain the dominant MI subtype (+ territory) per positive, for concordance.
  - Flag co-morbid MI (MI + another superclass) so it can be excluded from the
    concordance subset while remaining a valid classification positive.
"""

from __future__ import annotations

import pandas as pd

from .. import config


def _mi_codes_in(scp_codes: dict, min_likelihood: float) -> list[str]:
    """MI-superclass codes in a record meeting the likelihood threshold."""
    return [
        c for c in config.MI_CODES
        if c in scp_codes and scp_codes[c] >= min_likelihood
    ]


def _dominant_subtype(scp_codes: dict, mi_codes: list[str]) -> str | None:
    """Pick the reporting MI subtype: highest likelihood, ties broken by the
    canonical MI_CODES order (which runs most→least common)."""
    if not mi_codes:
        return None
    return max(mi_codes, key=lambda c: (scp_codes[c], -config.MI_CODES.index(c)))


def build_cohort(meta: pd.DataFrame, definition: str = config.LABEL_DEFINITION) -> pd.DataFrame:
    """Return the labelled cohort as a DataFrame indexed by ecg_id.

    Columns: label (1=MI, 0=NORM), mi_subtype, territory, comorbid,
    concordance_eligible, strat_fold. Records that are neither a valid MI
    positive nor a pure-NORM negative are dropped.
    """
    if definition not in ("strict", "inclusive"):
        raise ValueError(f"definition must be 'strict' or 'inclusive', got {definition!r}")
    min_like = config.STRICT_LIKELIHOOD if definition == "strict" else 0.0

    rows = []
    for ecg_id, r in meta.iterrows():
        codes, supers = r["scp_codes"], r["superclasses"]
        mi_codes = _mi_codes_in(codes, min_like)

        if mi_codes:  # ---- MI positive ----
            subtype = _dominant_subtype(codes, mi_codes)
            comorbid = supers - {"MI"} != set()
            territory = config.CODE_TO_TERRITORY.get(subtype)
            rows.append({
                "ecg_id": ecg_id, "label": 1, "mi_subtype": subtype,
                "territory": territory, "comorbid": comorbid,
                "concordance_eligible": (not comorbid) and territory is not None,
                "strat_fold": r["strat_fold"],
            })
        elif supers == {"NORM"}:  # ---- pure NORM negative ----
            rows.append({
                "ecg_id": ecg_id, "label": 0, "mi_subtype": None,
                "territory": None, "comorbid": False,
                "concordance_eligible": False, "strat_fold": r["strat_fold"],
            })
        # else: other-superclass-only or ambiguous -> dropped.

    return pd.DataFrame(rows).set_index("ecg_id")


def summarize_counts(meta: pd.DataFrame) -> pd.DataFrame:
    """Strict + inclusive MI/NORM counts per fold — replaces the docs/04 §6
    estimates with real numbers. Returns a tidy DataFrame."""
    frames = []
    for definition in ("strict", "inclusive"):
        cohort = build_cohort(meta, definition)
        g = (cohort.groupby(["strat_fold", "label"]).size()
             .unstack(fill_value=0).rename(columns={0: "NORM", 1: "MI"}))
        g["comorbid_MI"] = (cohort[cohort["comorbid"]]
                            .groupby("strat_fold").size().reindex(g.index, fill_value=0))
        g["definition"] = definition
        frames.append(g.reset_index())
    return pd.concat(frames, ignore_index=True)


def write_data_card(meta: pd.DataFrame, path=config.DATA_CARD) -> str:
    """Write docs/04 §7's required data card and return its text (Decision D15)."""
    counts = summarize_counts(meta)
    strict = build_cohort(meta, "strict")

    def _totals(defn):
        sub = counts[counts["definition"] == defn]
        return int(sub["MI"].sum()), int(sub["NORM"].sum()), int(sub["comorbid_MI"].sum())

    s_mi, s_norm, s_co = _totals("strict")
    i_mi, i_norm, i_co = _totals("inclusive")
    test = strict[strict["strat_fold"] == config.TEST_FOLD]

    lines = [
        "# PTB-XL Data Card — MI vs NORM",
        "",
        "Auto-generated by `src/data/build_cohort.py`. Every downstream result",
        "must trace back to these numbers and `config.SEED`.",
        "",
        f"- Task: **{config.TASK}**   Sampling rate: **{config.SAMPLING_RATE} Hz**"
        f"   ({config.SIGNAL_LENGTH} samples x {config.N_LEADS} leads)",
        f"- Label definition in force: **{config.LABEL_DEFINITION}** "
        f"(strict likelihood = {config.STRICT_LIKELIHOOD})",
        f"- Splits: train {config.TRAIN_FOLDS}, val {config.VAL_FOLD}, test {config.TEST_FOLD}",
        f"- Seed: {config.SEED}",
        "",
        "## Totals",
        "",
        "| definition | MI | NORM | co-morbid MI |",
        "|---|---|---|---|",
        f"| strict | {s_mi} | {s_norm} | {s_co} |",
        f"| inclusive | {i_mi} | {i_norm} | {i_co} |",
        "",
        f"Strict test-fold (fold {config.TEST_FOLD}) MI positives: "
        f"**{int((test['label'] == 1).sum())}**  "
        f"(concordance-eligible: {int(test['concordance_eligible'].sum())})",
        "",
        "## Per-fold counts",
        "",
        counts.to_markdown(index=False),
        "",
        "## MI subtype distribution (strict, concordance-eligible)",
        "",
        (strict[strict["concordance_eligible"]]["mi_subtype"]
         .value_counts().to_frame("n").to_markdown()),
        "",
    ]
    text = "\n".join(lines)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    return text


