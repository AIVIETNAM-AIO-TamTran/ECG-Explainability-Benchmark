"""
Phase 1 · Load PTB-XL metadata and signals.
See docs/02_EXECUTION_RUNBOOK.md (Phase 1) and docs/04_DATA_LABELS_AND_SAMPLING.md.

Responsibilities:
  - Read ptbxl_database.csv; parse `scp_codes` (stringified dict) with ast.literal_eval.
  - Read scp_statements.csv; expose the SCP-code -> diagnostic-superclass mapping.
  - Keep only the columns docs/04 section 2 marks as MUST-use / useful.
  - Load 100 Hz signals via wfdb using filename_lr, in the standard lead order.
"""

from __future__ import annotations

import ast
from pathlib import Path

import numpy as np
import pandas as pd

from .. import config

# Columns worth carrying forward (docs/04 §2). Missing ones are tolerated so the
# loader also works on the MIMIC-derived table, which shares only a subset.
_KEEP_COLUMNS = [
    "scp_codes", "strat_fold", "filename_lr", "filename_hr", "patient_id",
    "infarction_stadium1", "infarction_stadium2", "heart_axis",
    "validated_by_human", "second_opinion", "initial_autogenerated_report",
    "baseline_drift", "static_noise", "burst_noise", "electrodes_problems",
    "pacemaker", "extra_beats",
]


def _parse_scp_codes(value) -> dict:
    """`"{'IMI': 100.0, 'NORM': 0.0}"` -> ``{'IMI': 100.0, 'NORM': 0.0}``."""
    if isinstance(value, dict):
        return value
    if pd.isna(value):
        return {}
    return ast.literal_eval(value)


def load_scp_statements(ptbxl_dir: Path = config.PTBXL_DIR) -> pd.DataFrame:
    """Load scp_statements.csv (the code -> superclass lookup, docs/04 §3)."""
    path = Path(ptbxl_dir) / "scp_statements.csv"
    return pd.read_csv(path, index_col=0)


def build_superclass_map(scp_statements: pd.DataFrame) -> dict[str, str]:
    """Map each *diagnostic* SCP code to its superclass (NORM/MI/STTC/CD/HYP).

    Only rows flagged ``diagnostic == 1`` carry a ``diagnostic_class``; form and
    rhythm statements are excluded, matching Strodthoff's superclass utility.
    """
    diag = scp_statements[scp_statements["diagnostic"] == 1]
    return diag["diagnostic_class"].dropna().to_dict()


def load_metadata(ptbxl_dir: Path = config.PTBXL_DIR) -> pd.DataFrame:
    """Load ptbxl_database.csv indexed by ecg_id, with `scp_codes` parsed.

    Returns the trimmed metadata table plus two derived columns:
      - ``scp_codes``      : parsed dict of code -> likelihood.
      - ``superclasses``   : set of diagnostic superclasses present in the record.
    """
    ptbxl_dir = Path(ptbxl_dir)
    meta = pd.read_csv(ptbxl_dir / "ptbxl_database.csv", index_col="ecg_id")
    meta["scp_codes"] = meta["scp_codes"].apply(_parse_scp_codes)

    superclass_map = build_superclass_map(load_scp_statements(ptbxl_dir))
    meta["superclasses"] = meta["scp_codes"].apply(
        lambda codes: {superclass_map[c] for c in codes if c in superclass_map}
    )

    keep = [c for c in _KEEP_COLUMNS if c in meta.columns]
    return meta[keep + ["superclasses"]]


def load_signals(
    records: pd.DataFrame,
    ptbxl_dir: Path = config.PTBXL_DIR,
    sampling_rate: int = config.SAMPLING_RATE,
) -> np.ndarray:
    """Load raw signals for the rows in ``records`` -> array (n, 1000, 12).

    Uses ``filename_lr`` at 100 Hz (or ``filename_hr`` at 500 Hz). wfdb returns
    channels in the header order, which for PTB-XL already matches
    ``config.LEAD_ORDER``; we assert that rather than assume it silently.
    """
    import wfdb

    ptbxl_dir = Path(ptbxl_dir)
    col = "filename_lr" if sampling_rate == 100 else "filename_hr"

    signals = np.empty((len(records), config.SIGNAL_LENGTH, config.N_LEADS),
                       dtype=np.float32)
    for i, relpath in enumerate(records[col].to_numpy()):
        sig, meta = wfdb.rdsamp(str(ptbxl_dir / relpath))
        if i == 0 and list(meta["sig_name"]) != config.LEAD_ORDER:
            raise ValueError(
                f"Lead order {meta['sig_name']} != expected {config.LEAD_ORDER}; "
                "reorder before preprocessing."
            )
        signals[i] = sig.astype(np.float32)
    return signals
